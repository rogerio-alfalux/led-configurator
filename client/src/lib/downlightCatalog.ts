export function calculateDownlight(input: DownlightInput, catalog?: DownlightProduct[]): DownlightResult | null {
  const source = catalog ?? DOWNLIGHT_CATALOG;
  // Busca por SKU+Nome quando productName estiver definido (evita ambiguidade com SKUs duplicados)
  const product = input.productName !== undefined
    ? source.find(p => p.sku === input.productSku && p.name === input.productName)
    : source.find(p => p.sku === input.productSku);
  if (!product) return null;

  // Selecionar driver de acordo com o controle e a tensão
  let driver: DownlightDriver | null;
  if (input.controle === 'DIM DALI' && product.driverDimDali) {
    driver = product.driverDimDali;
  } else if (input.controle === 'DIM 1-10V' && product.driverDim110v) {
    driver = product.driverDim110v;
  } else if (input.controle === 'DIM TRIAC 110V' && product.driverDimTriac110v) {
    driver = product.driverDimTriac110v;
  } else if (input.controle === 'DIM TRIAC 220V' && product.driverDimTriac220v) {
    driver = product.driverDimTriac220v;
  } else if (input.tensao === 'Bivolt' && product.driverBivolt) {
    driver = product.driverBivolt;
  } else {
    driver = product.driver220 ?? null;
  }
  // Produto com lâmpada (sem driver, sem módulo LED): retornar resultado sem driver
  if (!driver && product.isLamp) {
    return {
      product,
      tensao: input.tensao,
      cct: input.cct,
      controle: input.controle,
      driver: { model: "", code: "" },
      ledModuleWithCCT: "",
      ledModuleQtd: null,
      ledModuleEq: null,
      oticaEq: null,
      oticaPrimariaEq: null,
      oticaSecundariaEq: null,
      dissipadorEq: null,
      holderEq: null,
    };
  }
  // Produto sem driver (peça avulsa): retornar resultado sem driver, usando módulo LED e CCT normalmente
  if (!driver && product.semDriver) {
    const cctKey = (input.cct ?? "").replace("K", "") as "2700" | "3000" | "4000" | "5000";
    const cctModuleField = `ledModule${cctKey}` as keyof typeof product;
    const cctModuleQtdField = `ledModuleQtd${cctKey}` as keyof typeof product;
    const cctEqField = `ledModuleEq${cctKey}` as keyof typeof product;
    const cctSpecificModule = product[cctModuleField] as string | null | undefined;
    const cctSpecificQtd = product[cctModuleQtdField] as number | null | undefined;
    const ledModuleWithCCT = cctSpecificModule
      ? cctSpecificModule.replace(/\[CCT\]/gi, input.cct).trim()
      : product.ledModule ? product.ledModule + " " + input.cct : "";
    return {
      product,
      tensao: (product.tensaoEmbutida ?? input.tensao) as "220V" | "Bivolt",
      cct: input.cct,
      controle: input.controle,
      driver: { model: "", code: "" },
      ledModuleWithCCT,
      ledModuleQtd: cctSpecificQtd ?? product.ledModuleQtd ?? null,
      ledModuleEq: (product[cctEqField] as string | null | undefined) ?? product.ledModuleEq ?? null,
      oticaEq: product.oticaEq ?? null,
      oticaPrimariaEq: product.oticaPrimariaEq ?? null,
      oticaSecundariaEq: product.oticaSecundariaEq ?? null,
      dissipadorEq: product.dissipadorEq ?? null,
      holderEq: product.holderEq ?? null,
    };
  }
  if (!driver) return null;

  // Produto RGBW: usar ledModule diretamente sem adicionar CCT
  let ledModuleWithCCT: string;
  let resolvedLedModuleQtd: number | null;
  let ledModuleEq: string | null;
  if (product.isRgbw) {
    // Para produtos RGBW, o módulo pode estar em ledModule genérico ou em um dos campos por CCT
    const rgbwModule = product.ledModule
      || product.ledModule2700
      || product.ledModule3000
      || product.ledModule4000
      || product.ledModule5000
      || "";
    ledModuleWithCCT = rgbwModule;
    resolvedLedModuleQtd = product.ledModuleQtd
      ?? product.ledModuleQtd2700
      ?? product.ledModuleQtd3000
      ?? product.ledModuleQtd4000
      ?? product.ledModuleQtd5000
      ?? null;
    ledModuleEq = null;
  } else {
    // Usar módulo LED específico por CCT quando disponível (novos campos da API)
    const cctKey = input.cct.replace("K", "") as "2700" | "3000" | "4000" | "5000";
    const cctModuleField = `ledModule${cctKey}` as keyof typeof product;
    const cctModuleQtdField = `ledModuleQtd${cctKey}` as keyof typeof product;
    const cctSpecificModule = product[cctModuleField] as string | null | undefined;
    const cctSpecificQtd = product[cctModuleQtdField] as number | null | undefined;
    // Se o módulo por CCT está disponível, usá-lo; senão, usar legado + CCT
    ledModuleWithCCT = cctSpecificModule
      ? cctSpecificModule.replace(/\[CCT\]/gi, input.cct).trim()
      : product.ledModule + " " + input.cct;
    resolvedLedModuleQtd = cctSpecificQtd ?? product.ledModuleQtd;
    const cctEqField = `ledModuleEq${cctKey}` as keyof typeof product;
    ledModuleEq = (product[cctEqField] as string | null | undefined) ?? null;
  }

  return {
    product,
    tensao: input.tensao,
    cct: input.cct,
    controle: input.controle,
    driver,
    ledModuleWithCCT,
    ledModuleQtd: resolvedLedModuleQtd,
    ledModuleEq,
    oticaEq: product.oticaEq ?? product.oticaPrimariaEq ?? null,
    oticaPrimariaEq: product.oticaPrimariaEq ?? null,
    oticaSecundariaEq: product.oticaSecundariaEq ?? null,
    dissipadorEq: product.dissipadorEq ?? null,
    holderEq: product.holderEq ?? null,
  };
}
