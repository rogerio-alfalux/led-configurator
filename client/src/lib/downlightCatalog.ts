export interface DownlightDriver {
  model: string;
  code: string;
  /** Corrente de programação do driver (ex: "350MA", "700MA"). null se não disponível. */
  corrente?: string | null;
}

export type ControleType = "ON/OFF" | "DIM 1-10V" | "DIM DALI" | "DIM TRIAC 110V" | "DIM TRIAC 220V";

export interface DownlightProduct {
  /** Tipo de instalacao: "EMBUTIR" | "NO FRAME" */
  instalacao: string;
  /** Familia do produto: LUNA, MYRO, AURA etc. */
  familia: string;
  /** SKU do produto */
  sku: string;
  /** Nome comercial do produto */
  name: string;
  /** Holder -- null se NAO APLICAVEL */
  holder: string | null;
  /** Quantidade numérica de holders (ex: 3). null quando não retornado pela API. */
  holderQtd: number | null;
  /** Otica legado (primaria + secundaria concatenadas) -- null se NAO APLICAVEL */
  otica: string | null;
  /** Ótica primária com quantidade embutida (ex: "9x LENTE DARKOO..."). null quando não retornado pela API. */
  oticaPrimaria: string | null;
  /** Ótica secundária com quantidade embutida (ex: "3x LOUVER..."). null quando não há. */
  oticaSecundaria: string | null;
  /** Dissipador -- null se NAO APLICAVEL */
  dissipador: string | null;
  /** Modulo LED (sem [CCT]) — campo legado */
  ledModule: string;
  /** Quantidade numérica de módulos LED (ex: 3). null quando não retornado pela API. */
  ledModuleQtd: number | null;
  /** Módulo LED específico por CCT (novos campos da API) */
  ledModule2700?: string | null;
  ledModule3000?: string | null;
  ledModule4000?: string | null;
  ledModule5000?: string | null;
  ledModuleQtd2700?: number | null;
  ledModuleQtd3000?: number | null;
  ledModuleQtd4000?: number | null;
  ledModuleQtd5000?: number | null;
  /** Código EQ do módulo por CCT — enriquecido pelo servidor */
  ledModuleEq2700?: string | null;
  ledModuleEq3000?: string | null;
  ledModuleEq4000?: string | null;
  ledModuleEq5000?: string | null;
  /** Código EQ do módulo LED genérico (para RGBW e legados) — enriquecido pelo servidor */
  ledModuleEq?: string | null;
  /** Código EQ da ótica genérica — enriquecido pelo servidor */
  oticaEq?: string | null;
  /** Código EQ da ótica primária — enriquecido pelo servidor */
  oticaPrimariaEq?: string | null;
  /** Código EQ da ótica secundária — enriquecido pelo servidor */
  oticaSecundariaEq?: string | null;
  /** Código EQ do dissipador — enriquecido pelo servidor */
  dissipadorEq?: string | null;
  /** Código EQ do holder — enriquecido pelo servidor */
  holderEq?: string | null;
  /** CCTs disponíveis para este produto (ex: ["3000K", "4000K"]) */
  ccts: string[];
  /** Driver para 220Vac -- null se nao houver opcao 220V */
  driver220: DownlightDriver | null;
  /** Driver para Bivolt -- null se nao houver opcao */
  driverBivolt: DownlightDriver | null;
  /** Driver DIM 1-10V -- null se nao disponivel */
  driverDim110v: DownlightDriver | null;
  /** Driver DIM DALI -- null se nao disponivel */
  driverDimDali: DownlightDriver | null;
  /** Driver DIM TRIAC 110V -- null se nao disponivel */
  driverDimTriac110v?: DownlightDriver | null;
  /** Driver DIM TRIAC 220V -- null se nao disponivel */
  driverDimTriac220v?: DownlightDriver | null;
  /** Quantidade de drivers ON/OFF 220V. null = driver não existe no produto. */
  driverQtd220: number | null;
  /** Quantidade de drivers Bivolt. null = driver não existe no produto. */
  driverQtdBivolt: number | null;
  /** Quantidade de drivers DIM 1-10V. null = driver não existe no produto. */
  driverQtdDim110v: number | null;
  /** Quantidade de drivers DIM DALI. null = driver não existe no produto. */
  driverQtdDimDali: number | null;
  /** Quantidade de drivers DIM TRIAC 110V. null = driver não existe no produto. */
  driverQtdDimTriac110v?: number | null;
  /** Quantidade de drivers DIM TRIAC 220V. null = driver não existe no produto. */
  driverQtdDimTriac220v?: number | null;
  /** Preço unitário ON/OFF 220V (R$). null = não cadastrado. */
  precoOnOff220?: number | null;
  /** Preço unitário ON/OFF Bivolt (R$). null = não cadastrado. */
  precoOnOffBivolt?: number | null;
  /** Preço unitário DIM 1-10V (R$). null = não cadastrado. */
  precoDim110v?: number | null;
  /** Preço unitário DIM DALI (R$). null = não cadastrado. */
  precoDimDali?: number | null;
  /** Preço unitário DIM TRIAC 110V (R$). null = não cadastrado. */
  precoDimTriac110v?: number | null;
  /** Preço unitário DIM TRIAC 220V (R$). null = não cadastrado. */
  precoDimTriac220v?: number | null;
  /** Produto sem driver: liga direto na tensão de rede ou usa lâmpada. Sem seleção de tensão/dim. */
  semDriver?: boolean;
  /** Tensão de rede embutida no produto (ex: "110V", "220V"). Exibida como informação, não como seleção. */
  tensaoEmbutida?: string | null;
  /** Custo do corpo ON/OFF 220V (para cálculo custo×markup) */
  custoCorpoOnoff220v?: number | null;
  /** Custo do corpo ON/OFF Bivolt */
  custoCorpoOnoffBivolt?: number | null;
  /** Custo do corpo DIM 1-10V */
  custoCorpoDim110v?: number | null;
  /** Custo do corpo DIM DALI */
  custoCorpoDimDali?: number | null;
  /** Custo do corpo DIM TRIAC 110V */
  custoCorpoDimTriac110v?: number | null;
  /** Custo do corpo DIM TRIAC 220V */
  custoCorpoDimTriac220v?: number | null;
  /** Markup padrão ON/OFF 220V */
  markupPadraoOnoff220v?: number | null;
  /** Markup padrão ON/OFF Bivolt */
  markupPadraoOnoffBivolt?: number | null;
  /** Markup padrão DIM 1-10V */
  markupPadraoDim110v?: number | null;
  /** Markup padrão DIM DALI */
  markupPadraoDimDali?: number | null;
  /** Markup padrão DIM TRIAC 110V */
  markupPadraoDimTriac110v?: number | null;
  /** Markup padrão DIM TRIAC 220V */
  markupPadraoDimTriac220v?: number | null;
  /** Markup mínimo ON/OFF 220V */
  markupMinimoOnoff220v?: number | null;
  /** Markup mínimo ON/OFF Bivolt */
  markupMinimoOnoffBivolt?: number | null;
  /** Markup mínimo DIM 1-10V */
  markupMinimoDim110v?: number | null;
  /** Markup mínimo DIM DALI */
  markupMinimoDimDali?: number | null;
  /** Markup mínimo DIM TRIAC 110V */
  markupMinimoDimTriac110v?: number | null;
  /** Markup mínimo DIM TRIAC 220V */
  markupMinimoDimTriac220v?: number | null;
  /** Produto com módulo RGBW (não tem CCT convencional 2700K-5000K). Exibe apenas opção "RGBW". */
  isRgbw?: boolean;
  /** Produto com lâmpada (sem módulo LED e sem driver). Não exibe seleção de CCT nem módulo LED. */
  isLamp?: boolean;
  /** Foto URL do produto */
  fotoUrl?: string | null;
}

export interface DownlightInput {
  /** SKU do produto (campo `sku` no catálogo) */
  productSku: string;
  /** Nome do produto -- combinado com productSku para identificar unicamente produtos com SKU duplicado */
  productName?: string;
  tensao: "220V" | "Bivolt";
  cct: string;
  controle: ControleType;
}

export interface DownlightResult {
  product: DownlightProduct;
  tensao: "220V" | "Bivolt";
  cct: string;
  controle: ControleType;
  driver: DownlightDriver;
  ledModuleWithCCT: string;
  /** Quantidade de módulos LED resolvida para o CCT selecionado */
  ledModuleQtd: number | null;
  /** Código EQ do módulo resolvido para o CCT selecionado (ex: "EQ00643"). null se não encontrado. */
  ledModuleEq: string | null;
  /** Código EQ da ótica (resolvida para o produto) */
  oticaEq: string | null;
  /** Código EQ da ótica primária */
  oticaPrimariaEq: string | null;
  /** Código EQ da ótica secundária */
  oticaSecundariaEq: string | null;
  /** Código EQ do dissipador */
  dissipadorEq: string | null;
  /** Código EQ do holder */
  holderEq: string | null;
}

